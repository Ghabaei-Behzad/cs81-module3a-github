//Function 1: Greet a user by name
function greet(name) { 
    return "Hello, " + name + "!"; 
}
//Function 2: is even
function isEven(num) { 
	return num % 2 === 0; 
}
//Function 3: square
function square(num) { 
	return num * num; 
}
//Function 4: convert temp.
function celsiusToFahrenheit(c) { 
	return (c * 9/5) + 32; 
}
//Function 5: string length
function getLength(str) { 
	return str.length; 
}

